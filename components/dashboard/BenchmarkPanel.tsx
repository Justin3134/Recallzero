function getScore(
  modelId: string,
  data: BenchmarkResults | null
): { f1: number; precision: number; recall: number; latency_ms: number } | null {
  const live = data?.models[modelId];
  if (live) return live;
  return PUBLISHED_BASELINES[modelId] ?? null;
}

