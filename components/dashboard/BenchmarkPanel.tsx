export function BenchmarkPanel() {
  const [activeTab, setActiveTab] = useState<BTab>("overview");
  const [benchData, setBenchData] = useState<BenchmarkResults | null>(null);
  const [cachedAt, setCachedAt] = useState<string | null>(null);
  const [running, setRunning] = useState(false);
  const [runError, setRunError] = useState<string | null>(null);
  const [f1Mode, setF1Mode] = useState<F1Mode>("partial");

  useEffect(() => {
    fetch("/api/benchmark")
      .then((r) => r.json())
      .then(
        (body: {
          data: BenchmarkResults | null;
          cachedAt?: string;
          pioneerJobMetrics?: PioneerJobMetrics | null;
        }) => {
          if (body.data) {
            setBenchData(body.data);
            setCachedAt(body.cachedAt ?? null);
          } else if (body.pioneerJobMetrics) {
            // No comparison run yet, but we have live Pioneer training metrics.
            // Store them in a minimal BenchmarkResults stub so EmptyState can show them.
            setBenchData({
              models: {},
              timestamp: new Date().toISOString(),
              examplesCount: 0,
              hasFineTunedModel: true,
              annotationCounts: {},
              pioneerJobMetrics: body.pioneerJobMetrics,
            });
          }
        }
      )
      .catch(() => {});
  }, []);

  const handleRun = useCallback(async () => {
    setRunning(true);
    setRunError(null);
    try {
      const res = await fetch("/api/benchmark", { method: "POST" });
      const body = (await res.json()) as {
        data?: BenchmarkResults;
        cachedAt?: string;
        error?: string;
      };
      if (!res.ok || body.error) {
        setRunError(body.error ?? `Error ${res.status}`);
      } else if (body.data) {
        setBenchData(body.data);
        setCachedAt(body.cachedAt ?? null);
      }
    } catch (e) {
      setRunError(e instanceof Error ? e.message : "Request failed");
    } finally {
      setRunning(false);
    }
  }, []);

  const hasFineTunedModel = benchData?.hasFineTunedModel ?? false;
  const pioneerJobMetrics = benchData?.pioneerJobMetrics ?? null;
  const hasComparisonData = Object.keys(benchData?.models ?? {}).length > 0;

  return (
    <div className="flex flex-col gap-5">
      {/* Header */}
      <div>
        <div className="flex items-center gap-2 mb-1">
          <BarChart3 size={11} className="text-white/30" />
          <h2 className="text-[11px] font-mono uppercase tracking-widest text-white/35">
            Model Benchmarks
          </h2>
        </div>
        <p className="text-[12px] text-white/40 leading-relaxed max-w-xl">
          Two-track evaluation: Pioneer&apos;s held-out validation metrics
          (authoritative, loaded automatically) plus a cross-model comparison on
          25 synthetic examples.
        </p>
      </div>

      {/* F1 mode toggle + eval control */}
      {activeTab !== "training" && (
        <div className="flex items-center justify-between gap-4 flex-wrap">
          <div className="flex items-center gap-2">
            <span className="text-[9px] font-mono uppercase tracking-widest text-white/25">
              F1 mode
            </span>
            <F1Toggle mode={f1Mode} onChange={setF1Mode} />
          </div>
          {hasComparisonData && (
            <EvalHeader
              data={benchData}
              cachedAt={cachedAt}
              running={running}
              error={runError}
              onRun={handleRun}
            />
          )}
        </div>
      )}

      {/* Sub-tabs */}
      <div className="flex gap-1 border-b border-white/[0.06]">
        {BTABS.map((t) => (
          <button
            key={t.id}
            onClick={() => setActiveTab(t.id)}
            className={cn(
              "flex items-center gap-1.5 px-3 py-2 text-[11px] font-medium rounded-t-lg transition-colors",
              activeTab === t.id
                ? "text-white/80 bg-white/[0.05] border border-b-0 border-white/[0.08]"
                : "text-white/30 hover:text-white/55"
            )}
          >
            <t.Icon size={10} />
            {t.label}
          </button>
        ))}
      </div>

      {/* Content */}
      <div>
        {running && activeTab !== "training" && (
          <div className="flex flex-col items-center gap-3 py-12 text-center">
            <RefreshCw size={20} className="text-white/20 animate-spin" />
            <p className="text-[12px] text-white/40">
              Running comparison across all models…
            </p>
            <p className="text-[10px] text-white/20 font-mono animate-pulse">
              GLiNER inference + LLM 5-shot · ~15–60s
            </p>
          </div>
        )}

        {!running && activeTab === "overview" && !hasComparisonData && (
          <EmptyState
            running={running}
            onRun={handleRun}
            pioneerJobMetrics={pioneerJobMetrics}
            baseScore={null}
          />
        )}

        {!running && hasComparisonData && activeTab === "overview" && (
          <OverviewTab
            data={benchData}
            pioneerJobMetrics={pioneerJobMetrics}
            f1Mode={f1Mode}
          />
        )}
        {!running && hasComparisonData && activeTab === "entities" && (
          <EntitiesTab data={benchData} f1Mode={f1Mode} />
        )}
        {!running && activeTab === "entities" && !hasComparisonData && (
          <div className="py-8 text-center">
            <p className="text-[11px] text-white/25">
              Run the comparison to see per-entity breakdown.
            </p>
          </div>
        )}
        {!running && activeTab === "speed" && (
          <SpeedTab data={hasComparisonData ? benchData : null} />
        )}
        {activeTab === "training" && (
          <TrainingTab hasFineTunedModel={hasFineTunedModel} />
        )}
      </div>
    </div>
  );
}
