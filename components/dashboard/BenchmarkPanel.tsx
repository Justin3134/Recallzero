// ── Eval header ───────────────────────────────────────────────────────────────

function EvalHeader({
  data,
  cachedAt,
  running,
  error,
  onRun,
}: {
  data: BenchmarkResults | null;
  cachedAt: string | null;
  running: boolean;
  error: string | null;
  onRun: () => void;
}) {
  function ageLabel() {
    if (!cachedAt) return null;
    const diffMs = Date.now() - new Date(cachedAt).getTime();
    const diffMin = Math.floor(diffMs / 60_000);
    if (diffMin < 1) return "just now";
    if (diffMin < 60) return `${diffMin}m ago`;
    return `${Math.floor(diffMin / 60)}h ago`;
  }

  return (
    <div className="flex items-center justify-between gap-4 flex-wrap">
      <div>
        {data ? (
          <p className="text-[10px] text-white/30 font-mono">
            Cross-model comparison · {data.examplesCount} synthetic examples ·
            last run {ageLabel()}
          </p>
        ) : (
          <p className="text-[10px] text-white/30 font-mono">
            No comparison run yet — click to run all models on synthetic eval set
          </p>
        )}
        {error && (
          <p className="text-[10px] text-red-400/70 mt-0.5">{error}</p>
        )}
      </div>
      <button
        onClick={onRun}
        disabled={running}
        className="flex items-center gap-1.5 px-3 py-1.5 rounded-lg border border-white/[0.12] bg-white/[0.04] text-[11px] font-medium text-white/60 hover:text-white hover:border-white/25 hover:bg-white/[0.08] transition-all disabled:opacity-40 disabled:cursor-not-allowed shrink-0"
      >
        <RefreshCw size={11} className={running ? "animate-spin" : ""} />
        {running
          ? "Running…"
          : data
          ? "Re-run comparison"
          : "Run comparison"}
      </button>
    </div>
  );
}

